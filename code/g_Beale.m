function g = g_Beale(x) 
    x1 = x(1);
    x2 = x(2);

    g1 = 2*x1*x2^6 + 2*x1*x2^4 + 5.25*x2^3 - 4*x1*x2^3 + 4.5*x2^2 - ...
        2*x1*x2^2 + 3*x2 - 4*x1*x2 + 6*x1 - 12.75;
    g2 = 6*x1^2*x2^5 + 4*x1^2*x2^3 - 6*x1^2*x2^2 - 2*x1^2*x2 - 2*x1^2 + ...
        15.75*x1*x2^2 + 9*x1*x2 + 3*x1;


    g = [g1; g2];
end
